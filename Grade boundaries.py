#Grade boundaries


score=int(input("Please enter your score: "))
if score > 80:
    print("You got an A-grade")
elif score > 70:
    print("You got a B-grade")
elif score > 60:
    print("You got a C-grade")
elif score > 50:
    print("You got a D-grade")
elif score > 40:
    print("You got an E-grade")
else:
    print("You got a U-grade")
    
